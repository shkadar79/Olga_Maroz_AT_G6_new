package homework.day2.base;

public class TrainMethodPrimitive {
    public void printInt(int incoming) {

        System.out.println("я получил на вход число" + " " + incoming);
    }

    public void printLong(long incoming) {

        System.out.println("я получил на вход длинное число" + " " + incoming);
    }

    public void printChar(char incoming) {
        System.out.println("я получил на вход символ" + " " + incoming);
    }

    public void printFloat(float incoming) {

        System.out.println("я получил на вход дробное число" + " " + incoming);
    }

    public void printDouble(double incoming) {
        System.out.println("я получил на вход длинное дробное число" + " " + incoming);
    }

    public void printShort(short incoming) {

        System.out.println("я получил на вход короткое число" + " " + incoming);
    }

    public void printByte(byte incoming) {

        System.out.println("я получил на вход очень короткое число" + " " + incoming);
    }

    public void printBoolean(boolean incoming) {
        System.out.println("я получил на вход булево" + " " + incoming);
    }

}
